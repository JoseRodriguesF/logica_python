#Faça um Programa que peça 2 números inteiros e um número real. Calcule e mostre:

numero1 = int(input("Digite um numero inteiro: "))
numero2 = int(input("Digite um numero inteiro: "))
numero3 = float(input("Digite um numero real: "))

resultado = numero1 + numero2 + numero3

print("Resultado: ", resultado)

#o produto do dobro do primeiro com metade do segundo .

resultado2 = (numero1*2)+(numero2/2)

print("Resultado: ",resultado2)


#a soma do triplo do primeiro com o terceiro.

resultado3 = (numero1*3)+numero3

print("Resultado: ",resultado3)

#o terceiro elevado ao cubo.

resultado4 = numero3**4

print("Resultado: ", resultado4)





